/*
    var - Ele é utilizado globalmente.
    let - Ele é gerado dentro de um escopo e pode trocar de dado futuramente.
    const - Ele é gerado dentro de um escopo porém não pode trocar de dados futuramente, o dado dele é constante.
 */

    var cachorro = "Kyara";
    console.log(cachorro);

    let lanche = "Pizza";
    console.log(lanche);

