/*
 "=": Diz que uma variável recebe um dado
 "+=": Diz que a variável tá recebendo um dado e somando também.
 "-=": Diz que a variável tá recebendo um dado e subtraindo.
 "*=": Diz que a variável tá recebendo um dado e multiplicando.
 "/=": Diz que a variável tá recebendo um dado e dividindo.
*/


let a = 1;
let b = 2;

console.log(`A = ${a}, B = ${b}`);

a += 2;

console.log(a);

b -= 1;

console.log(b);

a *= 2;

console.log(a);

b /= 2;

console.log(b);