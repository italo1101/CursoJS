// Number
console.log(123);

// String
console.log("123")

// vai subtrair uma string, isso pode dar problemas no código.
console.log("123" - 1);

// Aconselhável transformar String em Number

console.log(Number("123") - 1);

// Aqui ele diz qual o tipo do número, e no console vai sair: String.
console.log(typeof "123");
// E aqui vai sair: Number.
console.log(typeof Number("123"));

// Mesmo sendo o mesmo número, são tipos diferentes.

